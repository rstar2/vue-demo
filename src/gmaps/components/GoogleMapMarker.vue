<script>
export default {
  props: {
    google: {
      type: Object,
      required: true
    },
    map: {
      type: Object,
      required: true
    },
    marker: {
      type: Object,
      required: true
    },
    markerConfig: {
      type: Object,
      required: false
    }
  },

  mounted() {
    const { Marker } = this.google.maps;

    new Marker({
      position: this.marker.position,
      marker: this.marker,
      map: this.map,
      icon: this.markerConfig
    });
  },

  // template or render function is obligatory for each .vue component file
  render() {}
};
</script>
